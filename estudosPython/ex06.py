#Crie um algoritmo que leia um número que mostre o dobro, o triplo e a raiz quadrada

num = float(input("Digite um número: "))

dobro = num * 2
triplo = num * 3
raiz_quadrada = num ** 0.5

print("O número é:", num)
print("O dobro é:", dobro)
print("O triplo é:", triplo)
print("A raiz quadrada é:", raiz_quadrada)